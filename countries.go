func GetCountryWithFlag(countryName string) (string, string) {
	if countryName == "" {
		return "🌐", "🌐 Unknown"
	}

	clean := strings.ToLower(strings.TrimSpace(countryName))

	// ✅ 1. Full name exact match (most accurate)
	if flag, ok := flagMap[clean]; ok {
		return flag, flag + " " + countryName
	}

	// ✅ 2. Partial match - agar "South Korea" aaye to "south korea" dhundho
	for key, flag := range flagMap {
		if strings.Contains(clean, key) {
			return flag, flag + " " + countryName
		}
	}

	// ✅ 3. First word fallback
	words := strings.Fields(clean)
	if len(words) > 0 {
		if flag, ok := flagMap[words[0]]; ok {
			return flag, flag + " " + countryName
		}
	}

	return "🌐", "🌐 " + countryName
}


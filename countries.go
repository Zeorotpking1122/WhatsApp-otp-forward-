func GetCountryFromPhone(phone string) (string, string) {
	// Sirf digits rakho
	num := ""
	for _, c := range phone {
		if c >= '0' && c <= '9' {
			num += string(c)
		}
	}
	if len(num) == 0 {
		return "Unknown", "🌐"
	}

	// 3 digit se 1 digit tak match karo
	for _, d := range dialCodeMap {
		if strings.HasPrefix(num, d.code) {
			return d.country, d.flag
		}
	}
	return "Unknown", "🌐"
}
